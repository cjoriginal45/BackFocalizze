package com.focalizze.Focalizze.models;

/**
 * Enumeration defining types of trackable user interactions.
 * <p>
 * Enumeración que define los tipos de interacciones de usuario rastreables.
 */
public enum InteractionType {
    /**
     * User liked a thread.
     * El usuario dio "me gusta" a un hilo.
     */
    LIKE,

    /**
     * User commented on a thread.
     * El usuario comentó en un hilo.
     */
    COMMENT
}
